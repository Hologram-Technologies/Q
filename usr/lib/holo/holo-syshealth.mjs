// holo-syshealth.mjs — Q's awareness of the OS's OWN health (not just apps).
//
// The host publishes the boot/lifecycle signal at window.__holoLifecycle: { healthy, heals (renderer self-
// heals this boot), events (the holo-life beacon trail + host events: render terminations, ERR_INVALID_
// RESPONSE retries, CONTENT FAIL, first-paint…), paintMs, strandHead }. This module folds that into a small
// set of stable-id'd ISSUES that the autonomy spine reads on every tick (the `system` channel). So Q:
//   • MONITORS the OS itself — a boot wedge / renderer crash / serve storm becomes part of "what's true now",
//   • NOTIFIES in its own voice (the issue's message is handed straight to the courier → window.Q.note),
//   • can SELF-HEAL — each issue carries a reversible, TRUST-GATED action (rollback / reseal); Q proposes,
//     it does not act unless the operator has granted that topic (the system's "Q proposes" principle).
//
// Fail-soft: no signal ⇒ all-clear (no false alarms). Stable ids ⇒ a persistent condition is raised ONCE.

// CONFORMANCE — the holospaces-law attestation (generated by holo-conformance.mjs) folded into Q's health.
// Loaded fail-soft once at module init (no fetch; isomorphic ESM import). If a binding law regressed, Q says so;
// if the attestation is absent (unbuilt) or all-green, Q stays quiet (fail-soft = all-clear).
let _conf = null;
(async () => { try { const m = await import("./holo-conformance-attest.mjs"); _conf = m.CONFORMANCE || m.default || null; } catch (e) {} })();

function detect() {
  const out = [];
  if (_conf && _conf.localGreen === false) {
    const reds = (_conf.laws || []).filter((l) => l.status === "RED").map((l) => l.id);
    out.push({
      id: "conformance.regressed", severity: "high",
      msg: `Heads up: this build no longer satisfies ${reds.length ? "holospace law" + (reds.length > 1 ? "s" : "") + " " + reds.join(", ") : "a holospace law"}. I hold the release gate until it's green again — say the word and I'll show you which witness went red.`,
    });
  }
  // A machine κ opened in a tab (holo-machine-host) publishes its lifecycle to window.__holoMachine.
  // Checked FIRST (independent of the boot-lifecycle signal): a failed start NAMES ITSELF so Q notices;
  // a running/healthy machine raises nothing (fail-soft).
  let m = null;
  try { m = (typeof window !== "undefined") ? window.__holoMachine : null; } catch (e) {}
  if (m && m.state === "failed") {
    const who = m.guest || ((m.engine ? m.engine + " machine" : "a machine") + (m.kappa ? ` (κ ${m.kappa}…)` : ""));
    out.push({
      id: "machine.boot", severity: "high",
      msg: `The machine you opened (${who}) couldn't start${m.error ? ` — ${m.error}` : ""}. Its engine or snapshot may be off; reopening it usually clears it, and I can help dig in if it recurs.`,
    });
  }
  let lc = null;
  try { lc = (typeof window !== "undefined") ? window.__holoLifecycle : null; } catch (e) {}
  if (!lc || typeof lc !== "object") return out;
  const events = Array.isArray(lc.events) ? lc.events : [];
  const text = events.join("\n");
  // last holo-life beacon = the renderer's last reached boot step (the wedge locus if it never painted)
  const beacons = events.filter((e) => typeof e === "string" && e.indexOf("renderer:") === 0);
  const lastBeacon = beacons.length ? beacons[beacons.length - 1].replace(/^renderer:\s*\d+\s*/, "") : null;
  const painted = /first-paint/.test(text) || lc.healthy === true;

  if (lc.healthy === false || (lastBeacon && !painted)) {
    out.push({
      id: "boot.wedge", severity: "high", heal: "rollback",
      msg: lastBeacon
        ? `The desktop didn't finish starting — it stalled after "${lastBeacon}". I can roll back to the last version that started cleanly; just say the word.`
        : "The desktop had trouble starting this session. I can roll back to the last known-good version if it recurs.",
    });
  }
  if ((lc.heals | 0) > 0) {
    out.push({
      id: "boot.healed", severity: "low",
      msg: `Heads up: the desktop hit ${lc.heals | 0} hiccup${(lc.heals | 0) === 1 ? "" : "s"} while starting and I recovered ${(lc.heals | 0) === 1 ? "it" : "them"}. You're fine now — I'll flag it if it becomes a pattern.`,
    });
  }
  if (/CONTENT FAIL|ERR_INVALID_RESPONSE/.test(text)) {
    out.push({
      id: "serve.retry", severity: "med", heal: "reseal",
      msg: "The OS image was briefly hard to load at boot (it recovered). If this repeats, resealing or rolling back fixes it — I can do either.",
    });
  }
  return out;
}

// P0 (bare-metal ruler): name any subsystem running on a labelled FALLBACK — "not on metal here". Never
// alarming (low severity), stable-id'd so it's raised once, and it AUTO-CLEARS when the real path engages.
// Independent of the boot-lifecycle signal (runs even on plain web). CAPABILITY vs ACTUAL: window.__holoTiers
// is capability; __holoMachine / __holoTransport report the live tier a subsystem actually selected.
function metalFallbacks() {
  const out = [];
  try {
    const w = (typeof window !== "undefined") ? window : null;
    if (!w) return out;
    const T = w.__holoTiers;
    const softwareMsg = "Heads up: this device is drawing without a GPU (software rendering), so visuals fall back from the full 8K path. Everything works — it sharpens automatically on hardware with a GPU.";
    const present = w.__holoLensPresent || null;   // ACTUAL present path from the live projector (truth beats capability)
    if (present && /^webgpu/.test(present)) {
      /* actually on the metal (webgpu-superres / webgpu-lens) — nothing to name */
    } else if (present && /canvas2d/.test(present)) {
      // a projected tab is compositing on Canvas2D. If the device HAS a GPU, that's a fallback worth naming honestly.
      if (T && T.render === "webgpu") out.push({
        id: "metal.render.not-zero-copy", severity: "low",
        msg: "A projected tab is compositing on Canvas2D even though this device's GPU supports the faster zero-copy present — still smooth, just not full metal. It uses the GPU path where the surface allows.",
      });
      else out.push({ id: "metal.render.software", severity: "low", msg: softwareMsg });
    } else if (T && T.render === "software") out.push({
      id: "metal.render.software", severity: "low", msg: softwareMsg,
    });
    else if (T && T.render === "webgl") out.push({
      id: "metal.render.webgl", severity: "low",
      msg: "This session is on the WebGL path rather than WebGPU — still GPU-drawn, just not the fastest zero-copy present. It upgrades on its own when WebGPU is available.",
    });
    // ACTUAL compute tier: a machine κ running EMULATED (v86 / qemu-wasm / TCG) instead of on the host hypervisor.
    const m = w.__holoMachine;
    if (m && m.state !== "failed" && m.tier && /emul|wasm|tcg|v86/i.test(String(m.tier))) out.push({
      id: "metal.compute.emulated", severity: "low",
      msg: m.nativeAvailable
        ? `The machine you opened is running in compatibility mode (${m.tier}), but this device CAN run it on real hardware virtualization — faster and cooler. Want me to reopen it natively?`
        : `The machine you opened is running in compatibility mode (${m.tier}) rather than on hardware virtualization — correct, just not full native speed. If a native path becomes available I'll offer to switch.`,
    });
    // ACTUAL transport (P3): p2p/loopback are serverless (no note); relay/localhost-bridge have a server in the path.
    const tr = w.__holoTransport;
    if (tr && tr.tier === "relay") out.push({
      id: "metal.transport.relay", severity: "low",
      msg: "This connection is going through a relay rather than a direct device-to-device link. It works; a direct peer path is preferred and I switch to it when reachable.",
    });
    else if (tr && tr.tier === "localhost-bridge") out.push({
      id: "metal.transport.localhost-bridge", severity: "low",
      msg: "This runs through an on-device bridge (a local server on your machine) — private and local, but not yet pure peer-to-peer. The plan is to move it in-process so nothing has to listen on a port.",
    });
    // ACTUAL AI faculty tiers (P5): on-device (webgpu/webnn-npu/wasm-cpu/graph) is silent; stub/remote/CPU-despite-GPU named.
    const ai = w.__holoAi;
    if (ai && typeof ai === "object") {
      const gpuHere = !!(T && T.render === "webgpu");
      for (const f of Object.keys(ai)) {
        const rec = ai[f]; if (!rec || !rec.tier) continue;
        if (rec.tier === "stub") out.push({
          id: "metal.ai." + f + ".stub", severity: "low",
          msg: `Q's ${f} isn't wired to run on-device yet — it says so honestly rather than guessing, and lights up when the on-device model is bound.`,
        });
        else if (rec.tier === "remote") out.push({
          id: "metal.ai." + f + ".remote", severity: "low",
          msg: `Q's ${f} answered via the remote frontier model (Boost) — you enabled it, and it's governed and receipted, but it's a network call, not on-device this turn.`,
        });
        else if (rec.tier === "wasm-cpu" && gpuHere) out.push({
          id: "metal.ai." + f + ".cpu", severity: "low",
          msg: `Q's ${f} is running on the CPU floor even though this device has a GPU — correct, just not accelerated. It uses the GPU/NPU path when that model is bound.`,
        });
      }
    }
  } catch (e) {}
  return out;
}

// The `system` channel the spine reads: a bounded, stable-id'd issue list. Coherence folds it (breaks `whole`
// + dents the scalar), the observer phrases each as a "system.issue", the courier speaks it as Q.
export function issues() { try { return detect().concat(metalFallbacks()).slice(0, 8); } catch (e) { return []; } }

// A direct, plain-language surface for "Q, is the system OK?" — for humans AND agents.
export function summary() {
  const i = issues();
  if (!i.length) return "The OS is healthy — it started cleanly and nothing needs attention.";
  return i.map((x) => x.msg).join(" ");
}

// Q, is this a lawful holospace? — the POSITIVE surface the messenger shows. Reads the shipped, re-derivable
// attestation (fail-soft: honest "not attested yet" if unbuilt). Q.conformance() answers in plain words.
export function conformance() {
  if (!_conf) return { known: false, ok: null, line: "Conformance isn't attested in this build yet — run `holo conformance`." };
  const witnessed = (_conf.witnessedHere || []).length;
  return {
    known: true, ok: _conf.localGreen !== false, laws: _conf.laws || [], witnessed: _conf.witnessedHere || [], kappa: _conf.kappa || null,
    line: _conf.localGreen === false
      ? "This build has a holospace-law regression — I'm holding the gate until it's green."
      : `Holospace-conformant — ${witnessed} laws witnessed locally, the rest attested by the boot gate + holospaces CI. Don't trust it — re-derive: κ ${String(_conf.kappa || "").slice(0, 22)}…`,
  };
}

// Q.heal(action) — EXECUTE a proposed self-heal. TRUST-GATED by construction: rollback changes the live OS
// release, which the trust system caps at "ask" (never silent) — so this runs only on the operator's explicit
// invocation (Q proposes; the user says yes → this is called). It asks the host's MAIN-SHELL-ONLY holo:heal
// verb to flip `current` back to the previous painting release, then reloads to serve the rolled-back image.
export async function heal(action = "rollback") {
  if (action !== "rollback") return { ok: false, why: "only rollback is supported" };
  try {
    if (typeof window !== "undefined" && window.Q && window.Q.trust && typeof window.Q.trust.decide === "function") {
      const d = window.Q.trust.decide({ topic: "system-heal", kind: "publish" });
      if (d && (d.disposition === "never" || d.allow === false)) return { ok: false, why: "trust: not permitted" };
    }
  } catch (e) {}
  try {
    await new Promise((res, rej) => {
      if (typeof window === "undefined" || typeof window.cefQuery !== "function") return rej(new Error("no host bridge"));
      window.cefQuery({ request: "holo:heal:rollback", persistent: false, onSuccess: (s) => res(s), onFailure: (c, m) => rej(new Error(m || "heal-failed")) });
    });
    setTimeout(() => { try { location.reload(); } catch (e) {} }, 3500);   // serve the rolled-back image
    return { ok: true, action: "rollback", note: "rolling back to the last version that started cleanly — reloading…" };
  } catch (e) { return { ok: false, why: (e && e.message) || "heal-failed" }; }
}

if (typeof window !== "undefined") {
  window.HoloSysHealth = { issues, summary, detect, heal, conformance };
  // expose on Q's door when it's up (additive, idempotent): Q.health() answers; Q.heal() acts (confirm-gated);
  // Q.conformance() reports whether this is a lawful holospace (the attestation, re-derivable).
  const wire = () => { try { if (window.Q) { if (!window.Q.health) window.Q.health = summary; if (!window.Q.heal) window.Q.heal = heal; if (!window.Q.conformance) window.Q.conformance = conformance; return true; } } catch (e) {} return false; };
  if (!wire()) { let n = 0; const iv = setInterval(() => { if (wire() || ++n > 60) clearInterval(iv); }, 200); }
}
